    public boolean stdORfac(String u) throws SQLException
    {
        String query = "select user from stdprofile;";
        rs= st.executeQuery(query);
        while(rs.next())
        {
            if(rs.getString("user").equals(u))
            {
                return true;
            }
        }
        return false;
    }
